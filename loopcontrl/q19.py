##Write a program to take temperature in Celsius from user.
 ##If more than 40, print "Bhai pani lele, garmi maar degi 🔥", 
##else "Chill scene hai, weather cool hai ❄️".

temperature = int(input(" enter your temperature "))
if(temperature>40):
    print("Bhai pani lele, garmi maar degi 🔥")
else:
    print("Chill scene hai, weather cool hai ❄️")    