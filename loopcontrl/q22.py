##Write a program to take message status (seen/delivered) from user. If seen, print "Ab wait kar result ka 😶", 
##else "Dekhaa! maine bola tha, woh tujhe ghaas nahi dalegi 🤣".

message_status = input(" enter your message status (seen/delivered):   ")
if message_status == "seen":
    print("Ab wait kar result ka 😶")
else:
    print("Dekhaa! maine bola tha, woh tujhe ghaas nahi dalegi 🤣")    